T=[0 50 100 150 200]';
K=[579 647 684 690 668]';
Y=K
g0=ones(1,length(K))'
g1=T
g2=T.^2
g3=T.^3
G=[g0 g1 g2 g3];
GTG=G'*G
GTY=G'*Y

%Eliminação por gauss-jordan
I=eye(4)
n=[GTG GTY I]

n(1,:)=n(1,:)/n(1,1)
n(2,:)=n(2,:)-(n(2,1)/n(1,1)*n(1,:))
n(3,:)=n(3,:)-(n(3,1)/n(1,1)*n(1,:))
n(4,:)=n(4,:)-(n(4,1)/n(1,1)*n(1,:))

n(2,:)=n(2,:)/n(2,2)
n(1,:)=n(1,:)-(n(1,2)/n(2,2)*n(2,:))
n(3,:)=n(3,:)-(n(3,2)/n(2,2)*n(2,:))
n(4,:)=n(4,:)-(n(4,2)/n(2,2)*n(2,:))

n(3,:)=n(3,:)/n(3,3)
n(1,:)=n(1,:)-(n(1,3)/n(3,3)*n(3,:))
n(2,:)=n(2,:)-(n(2,3)/n(3,3)*n(3,:))
n(4,:)=n(4,:)-(n(4,3)/n(3,3)*n(3,:))

n(4,:)=n(4,:)/n(4,4)
n(1,:)=n(1,:)-(n(1,4)/n(4,4)*n(4,:))
n(2,:)=n(2,:)-(n(2,4)/n(4,4)*n(4,:))
n(3,:)=n(3,:)-(n(3,4)/n(4,4)*n(4,:))

b0=n(1,5)
b1=n(2,5)
b2=n(3,5)
b3=n(4,5)
b=[b0 b1 b2 b3]
flipud(b)'
[p]=polyfit(T,K,3)
plot(T,K,'ob')
hold 
tmod=[0:200]
kmod=b0+b1*tmod+b2*tmod.^2+b3*tmod.^3
plot(tmod,kmod,'-m')